package AdocaoPet;

public class Peixe extends Animal{
	public Peixe(){
		super();
		
	}
	public Peixe(String especie, int idade, float peso, String nome, char sexo){
		super();
		super.setEspecie(especie);
		super.setIdade(idade);
		super.setPeso(peso);
		super.setNome(nome);
		super.setSexo(sexo);
	}
	
}
