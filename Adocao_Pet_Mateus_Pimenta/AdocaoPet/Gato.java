package AdocaoPet;

public class Gato extends Animal{

	public Gato(){
		super();
		
	}
	public Gato(String especie, int idade, float peso, String nome, char sexo){
		super();
		super.setEspecie(especie);
		super.setIdade(idade);
		super.setPeso(peso);
		super.setNome(nome);
		super.setSexo(sexo);
	}
	
}

